DROP SEQUENCE NOTICEBOARDSEQ;
DROP TABLE NOTICEBOARD; 

CREATE SEQUENCE NOTICEBOARDSEQ;

CREATE TABLE NOTICEBOARD (
 NOTICEBOARD_NO NUMBER NOT NULL,
 NOTICEBOARD_ID VARCHAR2(200) NOT NULL,
 NOTICEBOARD_WRITER VARCHAR2(200) NOT NULL,
 NOTICEBOARD_TITLE VARCHAR2(300) NOT NULL,
 NOTICEBOARD_REGDATE DATE NOT NULL,
 NOICEBOARD_COUNT NUMBER,
 NOTICEBOARD_CONTENT VARCHAR2(2000) NOT NULL,
 CONSTRAINT NOTICEBOARD_PK PRIMARY KEY(NOTICEBOARD_NO)
 );

SELECT * FROM NOTICEBOARD;
 
 
